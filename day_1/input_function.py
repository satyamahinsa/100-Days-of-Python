# input() akan mengambil masukan dari user di konosol.
# Kemudian, print() akan mencetak kata "Hello" dan masukan dari user.
print("Hello, " + input("What's your name? "))

# Kode di bawah ini akan mencetak banyak karakter yang dimasukkan oleh user.
# len() akan menghitung banyak karakter tersebut.
print(len(input("What's your name? ")))