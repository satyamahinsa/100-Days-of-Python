# Rounding numbers using round().
# round() will round automatically (downward or upward) depending on the value behind the comma.
result = round(8 / 3, 2)
print(result)

# Rounding numbers using // (floor division).
# // will round down.
result = 8 // 3
print(result)

# Using shorthand in Python
score = 0
score += 5
score -= 1
score *= 2
score /= 4
print(score)

# Use of f-String.
name = "Gede"
score = 0
win = True
print(f"Hello, {name}. Your score is {score}. Do you win? {win}")